int		is_val(char *list)
{
	int i;

	i = 0;
	while (list[i] != '\0' && list[i])
	{
		if (list[i] < '0' && list[i] > '9')
			return (0);
		i++;
	}
	return (1);
}

int		o_val(char *list)
{
	int i;

	i = 0;
	while (list[i] != '\0' && list[i])
	{
		if (i >= 1)
			return (0);
		i++;
	}
	return (1);
}
