#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int i;
	unsigned char bit;

	i = 8;
	while (i--)
	{
		bit = (octet >> i & 1) + '0';
		write(1, &bit, 1);
	}
}

int		main(int argc, char **argv)
{
	argc = 1;
	print_bits(argv[1][0]);
	return (0);
}
